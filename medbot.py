def symptoms_response(intent):
    """ If we wanted to initialize the session to have some attributes we could
    add those here
    """
    session_attributes = {}
    card_title = "Welcome"
    if intent['slots']['symptomType']['value'] == 'cough':
        speech_output = "You need to drink hot water. Regular cough will subside in three to five days"
    elif intent['slots']['symptomType']['value'] == 'throat infection':
        speech_output = "Drink hot water on a regular basis, avoid cold drinks. Consult a professional in case of continued irritation."
    elif intent['slots']['symptomType']['value'] == 'fever':
        speech_output = "Take paracetamol as prescribed by professional. If it does not subside, consult a professional"
    elif intent['slots']['symptomType']['value'] == 'back pain':
        speech_output = "Sleep with stomach facing up. Avoid bending or lifting heavy objects"
    elif intent['slots']['symptomType']['value'] == 'headache':
        speech_output = "You have a headache, consult physician"
    elif intent['slots']['symptomType']['value'] == 'running nose':
        speech_output = "Common cold subsides in less than seven days. Drink warm water, avoid cold drinks"
    elif intent['slots']['symptomType']['value'] == 'dry scalp':
        speech_output = "Have regular head baths, avoid exposure to dusty environments"
    else:
        speech_output = "Consult a general physician."
    # If the user either does not reply to the welcome message or says something
    # that is not understood, they will be prompted again with this text.
    reprompt_text = "I don't know if you heard me, welcome to your custom alexa application!"
    should_end_session = False
    return build_response(session_attributes, build_speechlet_response(
        card_title, speech_output, reprompt_text, should_end_session))


def handle_session_end_request():
    card_title = "Session Ended"
    speech_output = "Thank you for trying the Alexa Skills Kit sample. " \
                    "Have a nice day! "
    # Setting this to true ends the session and exits the skill.
    should_end_session = True
    return build_response({}, build_speechlet_response(
        card_title, speech_output, None, should_end_session))

